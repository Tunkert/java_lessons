public class Variables002 {
    public static void main(String[] args) {
        String name = "Timothy Unkert"; // declare and initialize variable
        int age = 46; // declare and initialize variable
        /*
        We could also do short for my age
         */
        // combine these variables into something that outputs on the screen!
        System.out.println("Hi, my name is " + name + " and I am " + age + " years old.");

        // let's look at data types
        long worldPop = 7900000000L;

        // print the world population
        System.out.println("The world's population is " + worldPop + "!");


    }
}
